class Data8
{
	public static void main(String args[])
	{
		double a=67.123456000786;
		float b=(float)a;
		System.out.println(b);
		long c=(long)a;
		System.out.println(c);
		int d=(int)a;
		System.out.println(d);
		short e=(short)a;
		System.out.println(e);
		char f=(char)a;
		System.out.println(f);
		byte g=(byte)a;
		System.out.println(g);
	}
}