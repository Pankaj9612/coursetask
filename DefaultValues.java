class DefaultValues
{
	static int a;
	static byte b;
	static short c;
	static char d;
	static long e;
	static float f;
	static double g;
	static String h;
	public static void main(String[] args) {
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
	}
}	
