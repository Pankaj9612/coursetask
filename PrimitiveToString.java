package application;

public class PrimitiveToString {

	public static void main(String[] args) {
		
		int a = 10;
		String ans=String.valueOf(a);
		System.out.println(ans+20);
		
		boolean b=true;
		String ans1=String.valueOf(b);
		System.out.println(ans1+false);
		
		String ans2=String.valueOf('P');
		System.out.println(ans2+"ankaj");
				
	}
}
