class PreIncrement
{
	public static void main(String[] args) 
	{
		int coin=2;
		System.out.println(coin++);
		System.out.println(coin++);	
		System.out.println(coin);	
		System.out.println(coin++);	
		System.out.println(coin++);	
		System.out.println(coin);
		System.out.println(coin++);	
	}
}