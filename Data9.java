class Data9
{
	public static void main(String args[])
	{
		float a=45.45f;
		long c=(long)a;
		System.out.println(c);
		int d=(int)a;
		System.out.println(d);
		short e=(short)a;
		System.out.println(e);
		char f=(char)a;
		System.out.println(f);
		byte g=(byte)a;
		System.out.println(g);
	}
}